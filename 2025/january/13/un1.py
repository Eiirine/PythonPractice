# C помощью оператора and составьте два сложных логических выражения, одно из которых дает истину, другое – ложь.

result_true = (5 > 3) and (10 != 15)
print(result_true)
result_false = (5 < 3) and (10 != 15)
print(result_false)

# Аналогично выполните задачу 1, но уже с оператором or

result_true = (5 < 3) or (10 != 15)
print(result_true)
result_false = (5 < 3) or (10 == 15)
print(result_false)
