# Нарисуем на экране «прямоугольник» из двух видов символов.
# Края прямоугольника будут отрисованы символом «0», а внутренняя часть символом «1».


try:
    width, height = map(int, input("Введите ширину и высоту прямоугольника: ").split())
    if width % 1 !=0 or height % 1 !=0:
        raise ValueError
    # Построение прямоугольника
    for row in range(height):
        if row == 0 or row == height - 1:
            # Верхняя или нижняя граница: только символы '0'
            print("0" * width)
        else:
            # Средние строки: '0' по краям и '1' внутри
            print("0" + "1" * (width - 2) + "0")
except ValueError:
    print("Ошибка ввода! Пожалуйста, введите два целых числа.")